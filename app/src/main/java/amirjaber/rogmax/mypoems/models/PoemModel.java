package amirjaber.rogmax.mypoems.models;

public class PoemModel {

    private String name;
    private String poem;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPoem() {
        return poem;
    }

    public void setPoem(String poem) {
        this.poem = poem;
    }
}
